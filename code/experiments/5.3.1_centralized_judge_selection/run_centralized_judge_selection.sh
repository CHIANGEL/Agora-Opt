#!/bin/bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"
PYTHON_BIN="${PYTHON_BIN:-python3}"
EXPERIMENT_PY="${SCRIPT_DIR}/run_centralized_judge_selection.py"
OUTPUT_ROOT="${PROJECT_ROOT}/results/experiments/5.3.1_centralized_judge_selection"
MODEL_ROOT="${PROJECT_ROOT}/models"

# API credential template for Gemini / DeepSeek centralized judges.
: "${LLM_API_KEY:=PUT YOUR API KEY HERE}"
: "${LLM_API_BASE_URL:=PUT YOUR API URL HERE}"
export LLM_API_KEY
export LLM_API_BASE_URL

# Compatibility mirrors.
: "${OPENAI_API_KEY:=${LLM_API_KEY}}"
: "${OPENAI_BASE_URL:=${LLM_API_BASE_URL}}"
: "${API_KEY:=${LLM_API_KEY}}"
: "${API_URL:=${LLM_API_BASE_URL}}"
export OPENAI_API_KEY
export OPENAI_BASE_URL
export API_KEY
export API_URL

if [[ "${LLM_API_KEY}" == "PUT YOUR API KEY HERE" || "${LLM_API_BASE_URL}" == "PUT YOUR API URL HERE" ]]; then
    echo "Please set LLM_API_KEY and LLM_API_BASE_URL before running this experiment."
    exit 1
fi

# GenPRM / vLLM configuration. Fill these only if you run --experiments prm or both.
# Recommended placeholder path: ./models/
: "${PRM_MODEL_PATH:=${MODEL_ROOT}}"
: "${PRM_GPU_DEVICES:=0,1,2,3}"
: "${PRM_SERVER_PORT:=8001}"

# Initial-solution source files.
# Replace these placeholders with migrated result files under ./results/... .
COMPLEXLP_INITIAL_SOLUTION_A_JSONL="${COMPLEXLP_INITIAL_SOLUTION_A_JSONL:-PUT COMPLEXLP MODEL_A INITIAL_SOLUTION JSONL PATH HERE}"
COMPLEXLP_INITIAL_SOLUTION_A_EVAL_JSONL="${COMPLEXLP_INITIAL_SOLUTION_A_EVAL_JSONL:-PUT COMPLEXLP MODEL_A EVALUATION_RESULTS JSONL PATH HERE}"
COMPLEXLP_INITIAL_SOLUTION_B_JSONL="${COMPLEXLP_INITIAL_SOLUTION_B_JSONL:-PUT COMPLEXLP MODEL_B INITIAL_SOLUTION JSONL PATH HERE}"
COMPLEXLP_INITIAL_SOLUTION_B_EVAL_JSONL="${COMPLEXLP_INITIAL_SOLUTION_B_EVAL_JSONL:-PUT COMPLEXLP MODEL_B EVALUATION_RESULTS JSONL PATH HERE}"

INDUSTRYOR_INITIAL_SOLUTION_A_JSONL="${INDUSTRYOR_INITIAL_SOLUTION_A_JSONL:-PUT INDUSTRYOR MODEL_A INITIAL_SOLUTION JSONL PATH HERE}"
INDUSTRYOR_INITIAL_SOLUTION_A_EVAL_JSONL="${INDUSTRYOR_INITIAL_SOLUTION_A_EVAL_JSONL:-PUT INDUSTRYOR MODEL_A EVALUATION_RESULTS JSONL PATH HERE}"
INDUSTRYOR_INITIAL_SOLUTION_B_JSONL="${INDUSTRYOR_INITIAL_SOLUTION_B_JSONL:-PUT INDUSTRYOR MODEL_B INITIAL_SOLUTION JSONL PATH HERE}"
INDUSTRYOR_INITIAL_SOLUTION_B_EVAL_JSONL="${INDUSTRYOR_INITIAL_SOLUTION_B_EVAL_JSONL:-PUT INDUSTRYOR MODEL_B EVALUATION_RESULTS JSONL PATH HERE}"

PRIOPT_INITIAL_SOLUTION_A_JSONL="${PRIOPT_INITIAL_SOLUTION_A_JSONL:-PUT OPT-PRINCIPLED MODEL_A INITIAL_SOLUTION JSONL PATH HERE}"
PRIOPT_INITIAL_SOLUTION_A_EVAL_JSONL="${PRIOPT_INITIAL_SOLUTION_A_EVAL_JSONL:-PUT OPT-PRINCIPLED MODEL_A EVALUATION_RESULTS JSONL PATH HERE}"
PRIOPT_INITIAL_SOLUTION_B_JSONL="${PRIOPT_INITIAL_SOLUTION_B_JSONL:-PUT OPT-PRINCIPLED MODEL_B INITIAL_SOLUTION JSONL PATH HERE}"
PRIOPT_INITIAL_SOLUTION_B_EVAL_JSONL="${PRIOPT_INITIAL_SOLUTION_B_EVAL_JSONL:-PUT OPT-PRINCIPLED MODEL_B EVALUATION_RESULTS JSONL PATH HERE}"

for value in \
    "${COMPLEXLP_INITIAL_SOLUTION_A_JSONL}" "${COMPLEXLP_INITIAL_SOLUTION_A_EVAL_JSONL}" \
    "${COMPLEXLP_INITIAL_SOLUTION_B_JSONL}" "${COMPLEXLP_INITIAL_SOLUTION_B_EVAL_JSONL}" \
    "${INDUSTRYOR_INITIAL_SOLUTION_A_JSONL}" "${INDUSTRYOR_INITIAL_SOLUTION_A_EVAL_JSONL}" \
    "${INDUSTRYOR_INITIAL_SOLUTION_B_JSONL}" "${INDUSTRYOR_INITIAL_SOLUTION_B_EVAL_JSONL}" \
    "${PRIOPT_INITIAL_SOLUTION_A_JSONL}" "${PRIOPT_INITIAL_SOLUTION_A_EVAL_JSONL}" \
    "${PRIOPT_INITIAL_SOLUTION_B_JSONL}" "${PRIOPT_INITIAL_SOLUTION_B_EVAL_JSONL}"
do
    if [[ "${value}" == PUT* ]]; then
        echo "Please fill in all initial-solution and evaluation JSONL paths in run_centralized_judge_selection.sh."
        exit 1
    fi
done

EXPERIMENTS="${EXPERIMENTS:-both}"
MODEL_A_NAME="${MODEL_A_NAME:-deepseek-chat}"
MODEL_B_NAME="${MODEL_B_NAME:-gpt-4o}"

mkdir -p "${OUTPUT_ROOT}"

DATASET_ARGS=(
    --dataset-config "ComplexLP|ComplexLP|${COMPLEXLP_INITIAL_SOLUTION_A_JSONL}|${COMPLEXLP_INITIAL_SOLUTION_A_EVAL_JSONL}|${COMPLEXLP_INITIAL_SOLUTION_B_JSONL}|${COMPLEXLP_INITIAL_SOLUTION_B_EVAL_JSONL}"
    --dataset-config "IndustryOR|IndustryOR|${INDUSTRYOR_INITIAL_SOLUTION_A_JSONL}|${INDUSTRYOR_INITIAL_SOLUTION_A_EVAL_JSONL}|${INDUSTRYOR_INITIAL_SOLUTION_B_JSONL}|${INDUSTRYOR_INITIAL_SOLUTION_B_EVAL_JSONL}"
    --dataset-config "OPT-Principled|OPT-Principled|${PRIOPT_INITIAL_SOLUTION_A_JSONL}|${PRIOPT_INITIAL_SOLUTION_A_EVAL_JSONL}|${PRIOPT_INITIAL_SOLUTION_B_JSONL}|${PRIOPT_INITIAL_SOLUTION_B_EVAL_JSONL}"
)

CMD=(
    "${PYTHON_BIN}" "${EXPERIMENT_PY}"
    --output-root "${OUTPUT_ROOT}"
    --model-a-name "${MODEL_A_NAME}"
    --model-b-name "${MODEL_B_NAME}"
    --experiments "${EXPERIMENTS}"
    --model-path "${PRM_MODEL_PATH}"
    --gpu-devices "${PRM_GPU_DEVICES}"
    --port "${PRM_SERVER_PORT}"
)
CMD+=("${DATASET_ARGS[@]}")

echo "Running 5.3.1 centralized judge selection"
echo "Output root: ${OUTPUT_ROOT}"
echo "Experiments: ${EXPERIMENTS}"
echo "Local model path: ${PRM_MODEL_PATH}"
echo
"${CMD[@]}"
